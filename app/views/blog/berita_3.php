                    <p class="lead">Aktivitas - aktivitas apa aja sih yang dapat mengisi waktu kita selama karantina akibat Covid-19. Yuk simak artikel berikut!!</p>
                    <p><b>Jakarta</b> - Masyarakat diminta melakukan karantina mandiri paling tidak selama 14 hari demi meredam meredam penyebaran pandemi virus Corona penyebab COVID-19. Dalam kebijakan karantina mandiri itu, termasuk melakukan kegiatan bekerja, belajar, dan beribadah di rumah. Anjuran untuk tetap di rumah sebisa mungkin selalu dipatuhi agar penyebaran Corona tidak makin meluas dan segera berhenti.</p>

                    <blockquote>
                        <p>Hanya, tidak sedikit dari kita yang merasa bosan lantaran harus tetap di rumah selama masa karantina. Apa lagi bagi mereka yang dalam kesehariannya sering melakukan kegiatan di luar rumah maupun senang bertemu dengan orang banyak.</p>
                    </blockquote>

                    <p>Meski berpotensi menimbulkan kebosanan maupun gangguan kesehatan mental, tentu ada sisi baik dari masa karantina mandiri ini. Semisal, kita dapat lebih sering bertemu dengan keluarga. Kita bisa kembali menekuni hobi yang selama ini ditinggalkan akibat kesibukan sehari-sehari. Kita juga bisa melakukan beragam aktivitas bersama anggota keluarga lain, dengan tetap mengedepankan imbauan seperti menjaga jarak, rajin mencuci tangan, tidak berbagi peralatan makan, dan lain sebagainya.</p>
                    <p>Berikut, 10 kegiatan menarik yang bisa Anda lakukan di masa karantina mandiri akibat pandemi virus Corona.</p>

                    <p><b>1. Berkebun</b></p>
                    <p>Berkebun mungkin menjadi satu di antara hal yang dapat Anda lakukan bersama keluarga ketika masa karantina mandiri. Menanam tanaman bunga dan sayur-sayuran mungkin akan memberikan keuntungan juga di kemudian hari. Tidak hanya menanam bunga dan sayuran, Anda juga dapat membuat taman mini yang di dalamnya terdapat berbagai macam bunga dan kolam kecil yang berisikan ikan hias.</p>

                    <p><b>2. Piknik di Rumah</b></p>
                    <p>Siapkan selimut piknik dan makanan seolah-olah sedang piknik di rumah. Anda dapat menyiapkan berbagai jenis kue dan minuman, lalu menyantapnya bersama keluarga. Tambahkan makanan sehat dalam acara "piknik" Anda tersebut agar memberikan efek yang baik dari makanan yang Anda konsumsi.</p>
                    
                    <p><b>3. Membaca Buku</b></p>
                    <p>Membaca buku menjadi satu di antara cara untuk mengisi hari-hari selama karantina mandiri. Selain dapat memberikan ilmu baru, membaca buku juga dapat menjadikan waktu Anda tidak terbuang sia-sia.</p>

                    <p><b>4. Mendekor Rumah</b></p>
                    <p>Mendekor rumah menjadi satu di antara alternatif menghabiskan waktu di rumah. Mendekor rumah dapat memberikan suasan yang baru pada rumah Anda. Menghias pintu dengan berbagai pernak-pernik atau menggambar pada dinding kamar dapat menjadi beberapa pilihan yang dapat Anda lakukan dalam mendekorasi rumah.</p>
                    
                    <p><b>5. Memanggang Kue</b></p>
                    <p>Coba beberapa resep kue yang ada di internet. Resep kue yang menyehatkan lebih diutamakan untuk dicoba. </p>

                    <p><b>6. Mendengarkan Musik</b><p>
                    <p>Mendengarkan musik favorit bisa menghindarkan Anda dari stres. Selain itu, aktivitas mendengarkan musik juga dapat menenangkan hati dan memberikan inspirasi di kemudian hari.</p>

                    <p><b>7. Bermain Game</b></p>
                    <p>Menghilangkan stres juga tidak hanya bisa dilakukan dengan cara mendengarkan musik favorit. Bermain game dari ponsel atau konsol game seperti plastation atau Xbox, juga dapat membantu Anda tehindar dari stres.</p>

                    <p><b>8. Menulis Jurnal</b></p>
                    <p>Bagi Anda yang sangat menyukai menulis, melakukan hal ini dapat sangat membantu dalam melewati hari di rumah. Menulis jurnal dapat membunuh waktu begitu cepat karena fokus kita hanya pada apa yang akan kita tulis Menulis jurnal tentang kesehatan yang bersumber dari berbagai website mungkin menajadi opsi menarik di tengah wabah COVID-19 yang sedang terjadi saat ini.</p>

                    <p><b>9. Menonton Film</b></p>
                    <p>Menonton film bersama keluarga menjadi hal yang sangat menyenangkan untuk dilakukan di tengah karantina mandiri. Selain dapat menghibur diri bersama keluarga, kedekatan antaranggota keluarga makin terjalin erat. Menonton film drama mugnkin menjadi opsi yang tepat. Selain isi film drama yang biasanya tidak terlalu berat, menonton genre film ini biasanya disukai oleh seluruh anggota keluarga.</p>

                    <p><b>10. Karaoke</b></p>
                    <p>Selain kebersamaan yang terjalin, bernyanyi bersama keluarga dapat menjadi kegiatan yang positif karena Anda akan merasa terhibur dan terhindar dari depresi.</p>

