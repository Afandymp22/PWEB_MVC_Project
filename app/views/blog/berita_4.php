                    <p class="lead">Pemerintah memberikan Bantuan kouta internet untuk  memfasilitasi siswa, mahasiswa dan pengajar agar tetap bisa belejar via daring secara lancar</p>
                    <p><b>TRIBUNNEWS.COM</b> - Kementerian Pendidikan dan Kebudayaan (Kemendikbud) memberikan bantuan kuota internet.Rencananya dalam tiga bulan ke depan setiap tanggal 11 hingga 15, Kemendikbud akan menyalurkan bantuan kuota internet untuk peserta didik.Mulai dari PAUD, SD, SMP, SMA hingga Mahasiswa.</p>

                    <blockquote>
                        <p>Bantuan juga diberikan untuk para tenaga pengajar dari guru hingga dosen. Peserta didik dari tingkat Pendidikan Anak Usia Dini (PAUD) hingga jenjang perkuliahan akan mendapatkan bantuan kuota data internet gratis pada 2021.</p>
                    </blockquote>

                    <p>Besaran bantuan Kuota Kemendikbud pada tahun 2021, adalah sebagai berikut :</p>
                    <p>
                        <ul>
                            <li>Peserta didik PAUD : 7 GB per bulan</li>
                            <li>Peserta didik SD, SMP, SMA : 10 GB per bulan</li>
                            <li>Tenaga pendidik PAUD, SD, SMP, SMA : 12 GB per bulan</li>
                            <li>Mahasiswa dan Dosen : 15 GB per bulan</li>
                        </ul>
                    </p>

                    <p>Kuota yang diberikan merupakan kuota umum, namun ada beberapa laman yang tidak bisa diakses yakni :</p>
                    <p>
                        <ul>
                            <li>Twitter</li>
                            <li>Instagram</li>
                            <li>Facebook</li>
                            <li>Tiktok</li>
                            <li>Situs yang diblokir oleh Kominfo</li>
                        </ul>
                    </p>

                    <p>Menteri Pendidikan dan Kebudayaan, Nadiem Makarim mengungkapkan, berdasarkan masukan masyarakat, keseluruhan bantuan kuota data internet pada 2021 merupakan kuota umum. Sehingga dapat digunakan untuk mengakses semua laman dan aplikasi.
                        Kecuali, laman dan aplikasi yang sudah diblokir oleh Kementerian Komunikasi dan Informatika (Kemenkominfo) serta daftar pengecualian yang tercantum pada situs resmi bantuan kuota data internet.
                    </p>
                    <p>Adapun peserta didik dan pendidik yang menerima bantuan kuota adalah semua yang telah menerima bantuan kuota pada bulan November-Desember 2020 dan nomornya masih aktif.</p>
                    <p>"Otomatis mereka akan menerima bantuan kuota pada bulan Maret 2021.","Kecuali yang total penggunaannya kuotanya kurang dari 1GB," tegas <b>Nadiem</b>.</p>
                    <p>Selain itu, untuk yang sudah menerima bantuan pada bulan November-Desember 2020 maka pemimpin satuan pendidikan tidak perlu mengunggah SPTJM lagi.Sementara itu dikutip dari laman setkab.go.id, apabila ada yang nomornya berubah atau belum menerima bantuan kuota sebelumnya, calon penerima harus melapor kepada pimpinan satuan pendidikan sebelum bulan April 2021 untuk mendapat bantuan kuota. Selanjutnya, pimpinan/operator satuan pendidikan mengunggah SPTJM untuk nomor yang berubah atau nomor baru.</p>
                    <p>Laman yang perlu diakses untuk PAUD, SD, SMP, dan SMA ialah: <a href="https://vervalponsel.data.kemdikbud.go.id">https://vervalponsel.data.kemdikbud.go.id</a></p>
                    <p>Sedangkan untuk jenjang pendidikan tinggi melalui laman: <br><a href="https://pddikti.kemdikbud.go.id">https://pddikti.kemdikbud.go.id.</a></p>
